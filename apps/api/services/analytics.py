class NumberedCanvas(canvas.Canvas):
    """
    Two-pass canvas pattern to compute total page count dynamically
    and print 'Page X of Y' in the running footer.
    """
    def __init__(self, *args, **kwargs):
        super().__init__(*args, **kwargs)
        self._saved_page_states = []

    def showPage(self):
        self._saved_page_states.append(dict(self.__dict__))
        self._startPage()

    def save(self):
        num_pages = len(self._saved_page_states)
        for state in self._saved_page_states:
            self.__dict__.update(state)
            self.draw_page_number(num_pages)
            super().showPage()
        super().save()

    def draw_page_number(self, page_count):
        if self._pageNumber == 1:
            return  # Suppress on cover page
        self.saveState()
        self.setFont("Helvetica-Bold", 8)
        self.setFillColor(colors.HexColor("#475569"))
        
        # Header
        self.drawString(54, 750, "CAREERPILOT AI — PROFESSIONAL PERFORMANCE METRICS")
        self.setFont("Helvetica", 8)
        self.drawRightString(558, 750, datetime.now().strftime("%Y-%m-%d"))
        
        # Header Rule
        self.setStrokeColor(colors.HexColor("#e2e8f0"))
        self.setLineWidth(0.75)
        self.line(54, 742, 558, 742)
        
        # Footer Rule
        self.line(54, 55, 558, 55)
        
        # Footer
        self.setFont("Helvetica", 8)
        self.drawString(54, 40, "Confidential • Generated by CareerPilot AI")
        self.drawRightString(558, 40, f"Page {self._pageNumber} of {page_count}")
        self.restoreState()


