    def _generate_excel_file(self, file_path: str, report: AnalyticsReport):
        """
        Creates styled Excel sheets containing breakdown scores, snapshots, and SWOT insights.
        """
        wb = openpyxl.Workbook()
        
        # ----------------------------------------------------
        # SHEET 1: READINESS BREAKDOWN
        # ----------------------------------------------------
        ws1 = wb.active
        ws1.title = "Readiness Breakdown"
        ws1.views.sheetView[0].showGridLines = True
        
        # Define Styling elements
        navy_header_fill = PatternFill(start_color="1E3A8A", end_color="1E3A8A", fill_type="solid")
        title_font = Font(name="Calibri", size=16, bold=True, color="1E3A8A")
        header_font = Font(name="Calibri", size=11, bold=True, color="FFFFFF")
        bold_font = Font(name="Calibri", size=11, bold=True)
        regular_font = Font(name="Calibri", size=11)
        align_left = Alignment(horizontal="left", vertical="center")
        align_right = Alignment(horizontal="right", vertical="center")
        align_center = Alignment(horizontal="center", vertical="center")
        
        thin_border = Border(
            left=Side(style='thin', color='CBD5E1'),
            right=Side(style='thin', color='CBD5E1'),
            top=Side(style='thin', color='CBD5E1'),
            bottom=Side(style='thin', color='CBD5E1')
        )
        double_bottom_border = Border(
            top=Side(style='thin', color='000000'),
            bottom=Side(style='double', color='000000')
        )

        ws1.cell(row=1, column=1, value=report.title).font = title_font
        ws1.cell(row=2, column=1, value=f"Generated: {report.data['generated_date']}").font = Font(name="Calibri", size=10, italic=True)
        
        headers = ["Category", "Weight", "Score", "Status Check", "Description"]
        for col_idx, header in enumerate(headers, 1):
            cell = ws1.cell(row=4, column=col_idx, value=header)
            cell.font = header_font
            cell.fill = navy_header_fill
            cell.alignment = align_center if col_idx != 5 else align_left
            cell.border = thin_border
            
        breakdown = report.data["breakdown"]
        categories = [
            ("Resume Quality", breakdown["resume_quality"]),
            ("Skill Coverage", breakdown["skill_coverage"]),
            ("Goal Alignment", breakdown["career_alignment"]),
            ("Roadmap Progress", breakdown["roadmap_progress"]),
            ("Interview Readiness", breakdown["interview_performance"]),
            ("Consistency Metrics", breakdown["learning_consistency"])
        ]
        
        current_row = 5
        for name, details in categories:
            ws1.cell(row=current_row, column=1, value=name).font = bold_font
            ws1.cell(row=current_row, column=2, value=details["weight"]).font = regular_font
            ws1.cell(row=current_row, column=3, value=details["score"]).font = regular_font
            ws1.cell(row=current_row, column=4, value=details["status"]).font = regular_font
            ws1.cell(row=current_row, column=5, value=details["description"]).font = regular_font
            
            # Format percentages and numbers
            ws1.cell(row=current_row, column=2).number_format = '0%'
            ws1.cell(row=current_row, column=3).number_format = '0'
            
            for c in range(1, 6):
                cell = ws1.cell(row=current_row, column=c)
                cell.border = thin_border
                cell.alignment = align_left if c in [1, 4, 5] else align_center
            current_row += 1
            
        # Summary total row
        ws1.cell(row=current_row, column=1, value="Career Readiness Score").font = bold_font
        total_cell = ws1.cell(row=current_row, column=3, value=breakdown["career_readiness_score"])
        total_cell.font = bold_font
        total_cell.alignment = align_center
        
        for col in range(1, 6):
            ws1.cell(row=current_row, column=col).border = double_bottom_border

        # ----------------------------------------------------
        # SHEET 2: SWOT INSIGHTS
        # ----------------------------------------------------
        ws2 = wb.create_sheet(title="SWOT Insights")
        ws2.views.sheetView[0].showGridLines = True
        ws2.cell(row=1, column=1, value="SWOT Analysis & Recommendations").font = title_font
        
        insights = report.data["insights"]
        
        # Write strengths
        ws2.cell(row=3, column=1, value="Strengths").font = bold_font
        for idx, s in enumerate(insights["strengths"], 4):
            ws2.cell(row=idx, column=1, value=f"• {s}").font = regular_font
            
        # Write weaknesses
        ws2.cell(row=3, column=3, value="Weaknesses").font = bold_font
        for idx, w in enumerate(insights["weaknesses"], 4):
            ws2.cell(row=idx, column=3, value=f"• {w}").font = regular_font

        # Write opportunities
        ws2.cell(row=8, column=1, value="Opportunities").font = bold_font
        for idx, o in enumerate(insights["opportunities"], 9):
            ws2.cell(row=idx, column=1, value=f"• {o}").font = regular_font

        # Write risks
        ws2.cell(row=8, column=3, value="Risks").font = bold_font
        for idx, r in enumerate(insights["risks"], 9):
            ws2.cell(row=idx, column=3, value=f"• {r}").font = regular_font

        # Auto-adjust column widths across sheets
        for sheet in [ws1, ws2]:
            for col in sheet.columns:
                max_len = 0
                for cell in col:
                    val = str(cell.value or '')
                    if '\n' in val:
                        val = max(val.split('\n'), key=len)
                    max_len = max(max_len, len(val))
                col_letter = get_column_letter(col[0].column)
                sheet.column_dimensions[col_letter].width = max(max_len + 3, 12)
                
        wb.save(file_path)

    def _generate_csv_file(self, file_path: str, report: AnalyticsReport):
        """
        Exports a flat CSV row structure.
        """
        breakdown = report.data["breakdown"]
        with open(file_path, "w", newline="", encoding="utf-8") as f:
            writer = csv.writer(f)
            writer.writerow(["Metadata Type", "Category", "Score / Weight Value", "Status Status", "Details"])
            writer.writerow(["Overview", "Title", report.title, "", ""])
            writer.writerow(["Overview", "Readiness Score", breakdown["career_readiness_score"], "", "Overall Unified Score"])
            writer.writerow(["Overview", "Growth Score", breakdown["overall_growth_score"], "", "Growth Score"])
            
            categories = [
                ("Resume Score", breakdown["resume_quality"]),
                ("Skill Score", breakdown["skill_coverage"]),
                ("Career Score", breakdown["career_alignment"]),
                ("Roadmap Score", breakdown["roadmap_progress"]),
                ("Interview Score", breakdown["interview_performance"]),
                ("Consistency Score", breakdown["learning_consistency"])
            ]
            for label, obj in categories:
                writer.writerow(["Metric Breakdown", label, obj["score"], obj["status"], obj["description"]])

