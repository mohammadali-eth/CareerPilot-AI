    async def calculate_raw_metrics(self, user_id: UUID) -> Dict[str, Any]:
        """
        Gathers raw data points from all subsystems and evaluates individual scores (0-100).
        """
        # 1. Resume Score
        resume_score = 0.0
        resumes = await self.resume_repo.get_by_user_id(user_id)
        latest_resume_id = None
        if resumes:
            latest_resume = resumes[0]
            latest_resume_id = latest_resume.id
            if latest_resume.scores:
                resume_score = float(latest_resume.scores[0].overall_score)
            else:
                resume_score = 55.0  # Baseline for parsed text with no score entry

        # 2. ATS Score & Skill Gap Score
        ats_score = 0.0
        skill_gap_score = 0.0
        if latest_resume_id:
            ats_reports = await self.ats_repo.get_latest_by_resume_id(latest_resume_id)
            if ats_reports:
                ats_score = float(ats_reports.ats_score)
                # Deduct score based on missing keywords count
                gaps = ats_reports.missing_keywords or []
                skill_gap_score = max(0.0, 100.0 - (len(gaps) * 8.0))
            else:
                ats_score = 50.0
                skill_gap_score = 50.0
        else:
            ats_score = 0.0
            skill_gap_score = 0.0

        # 3. Career Match Score
        career_match_score = 0.0
        recs = await self.career_repo.get_by_user_id(user_id)
        if recs and recs[0].matches:
            # Take average of top 3 career matches
            top_matches = recs[0].matches[:3]
            career_match_score = sum(m.match_score for m in top_matches) / len(top_matches)
        else:
            career_match_score = 0.0

        # 4. Roadmap Completion
        roadmap_completion = 0.0
        roadmaps = await self.roadmap_repo.get_by_user_id(user_id)
        if roadmaps:
            latest_roadmap = roadmaps[0]
            milestones = latest_roadmap.milestones
            if milestones:
                roadmap_completion = sum(m.completion_percentage for m in milestones) / len(milestones)
            else:
                roadmap_completion = 0.0

        # 5. Interview Readiness
        interview_readiness = 0.0
        interviews = await self.interview_repo.get_by_user_id(user_id)
        completed_interviews = [i for i in interviews if i.status == "completed"]
        if completed_interviews:
            latest_interview = completed_interviews[0]
            interview_readiness = float(latest_interview.readiness_score or latest_interview.score or 0)
        else:
            interview_readiness = 0.0

        # 6. Learning Streak & Consistency
        learning_streak = 0
        # Calculate active streak based on consecutive days of activity in last 14 days
        active_dates = set()
        # Add roadmap completion timestamps
        if roadmaps:
            for record in roadmaps[0].progress_records:
                active_dates.add(record.created_at.date())
        # Add interview attempts
        for i in interviews:
            active_dates.add(i.started_at.date())
        
        # Calculate consecutive streak ending today
        today = date.today()
        current_date = today
        while current_date in active_dates:
            learning_streak += 1
            current_date -= timedelta(days=1)
            
        # Fallback to activity counts if no consecutive dates exist
        if learning_streak == 0:
            learning_streak = min(7, len(active_dates))

        return {
            "resume_score": resume_score,
            "ats_score": ats_score,
            "career_match_score": career_match_score,
            "skill_gap_score": skill_gap_score,
            "roadmap_completion": roadmap_completion,
            "interview_readiness": interview_readiness,
            "learning_streak": learning_streak,
        }

    async def get_or_create_latest_metrics(self, user_id: UUID) -> UserMetric:
        """
        Computes, caches, and returns user metrics row.
        """
        raw = await self.calculate_raw_metrics(user_id)
        
        # Career Readiness Score Formula:
        # Resume Quality (15%) + Average ATS & Skill Gap (20%) + Career Match (15%) + Roadmap Progress (20%) + Interview (20%) + Learning Streak (10%)
        learning_streak_score = min(10.0, float(raw["learning_streak"])) * 10.0
        avg_skill_coverage = (raw["ats_score"] + raw["skill_gap_score"]) / 2.0
        
        readiness_score = (
            (raw["resume_score"] * 0.15) +
            (avg_skill_coverage * 0.20) +
            (raw["career_match_score"] * 0.15) +
            (raw["roadmap_completion"] * 0.20) +
            (raw["interview_readiness"] * 0.20) +
            (learning_streak_score * 0.10)
        )
        
        # Overall Growth Score calculation relative to baseline
        overall_growth = max(0.0, readiness_score - 20.0)

        existing = await self.metric_repo.get_by_user_id(user_id)
        metric_data = {
            "user_id": user_id,
            "resume_score": raw["resume_score"],
            "ats_score": raw["ats_score"],
            "career_match_score": raw["career_match_score"],
            "skill_gap_score": raw["skill_gap_score"],
            "roadmap_completion": raw["roadmap_completion"],
            "interview_readiness": raw["interview_readiness"],
            "learning_streak": raw["learning_streak"],
            "career_readiness_score": round(readiness_score, 1),
            "overall_growth_score": round(overall_growth, 1),
        }

        if existing:
            return await self.metric_repo.update(db_obj=existing, obj_in=metric_data)
        else:
            return await self.metric_repo.create(obj_in=metric_data)

