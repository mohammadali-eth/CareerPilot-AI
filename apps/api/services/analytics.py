    async def export_report(self, user_id: UUID, report_id: UUID, export_type: str) -> Dict[str, Any]:
        """
        Export generated report to PDF, CSV, Excel, or Markdown.
        """
        report = await self.report_repo.get_by_id_and_user_id(report_id, user_id)
        if not report:
            raise ValueError("Report not found or access denied.")

        export_dir = os.path.abspath("apps/api/exports")
        os.makedirs(export_dir, exist_ok=True)
        
        filename = f"careerpilot_report_{report.id}.{export_type.lower()}"
        file_path = os.path.join(export_dir, filename)
        export_url = f"/api/v1/analytics/exports/download/{filename}"

        if export_type.lower() == "pdf":
            self._generate_pdf_file(file_path, report)
        elif export_type.lower() == "excel":
            self._generate_excel_file(file_path, report)
        elif export_type.lower() == "csv":
            self._generate_csv_file(file_path, report)
        elif export_type.lower() == "markdown":
            self._generate_markdown_file(file_path, report)
        else:
            raise ValueError(f"Unsupported export format: {export_type}")

        export_obj = await self.export_repo.create(obj_in={
            "user_id": user_id,
            "report_id": report.id,
            "export_type": export_type.lower(),
            "export_url": export_url
        })

        return {
            "id": export_obj.id,
            "report_id": report.id,
            "export_type": export_type.lower(),
            "export_url": export_url,
            "created_at": export_obj.created_at
        }

